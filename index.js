// Function Expression for divide
const divide = function(a, b) {
    return a / b;
};

// Call the divide function with 2000 and 100
console.log(divide(2000, 100)); // Should return 20

// Arrow Function for square
const square = x => x * x;

// Example usage
console.log(square(5)); // Should return 25

// Existing functions
const add = (x, y) => x + y;

// Testing existing functions
console.log(add(2, 3)); // Should return 5

